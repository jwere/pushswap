#include "libft.h"

int		ft_nbrlen(int nbr)
{
	int nb;
	int i;

	i = 0;
	if (nbr == -2147483648)
		return (11);
	if (nbr < 0)
	{
		nbr = -nbr;
		i++;
	}
	if (nbr == 0)
		return (1);
	nb = nbr;
	while (nb > 0)
	{
		nb = nbr / 10;
		nbr = nb;
		i++;
	}
	return (i);
}
