#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	swap;

	j = -1;
	i = 0;
	while (s[i])
		i++;
	while (--i > ++j)
	{
		swap = s[j];
		s[j] = s[i];
		s[i] = swap;
	}
	return (s);
}
