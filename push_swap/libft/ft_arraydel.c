#include "libft.h"

void	ft_arraydel(char **arr)
{
	int i;

	i = 0;
	while (arr[i])
		i++;
	i--;
	while (i >= 0)
	{
		ft_strdel(&arr[i]);
		i--;
	}
	free(arr);
	arr = NULL;
}
