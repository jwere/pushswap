#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;

	i = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		str++;
	if (*str == '-')
		n = -1;
	else
		n = 1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str && ft_isdigit(*str))
		i = i * 10 + (*str++ - '0');
	return (i * n);
}
